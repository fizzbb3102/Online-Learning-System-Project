    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        Account currentUser = (Account) session.getAttribute("currentUser");
        if (currentUser != null) {
            AccountDAO accDb = new AccountDAO();
            Account info = accDb.getAccountInfo(currentUser);
            request.setAttribute("info", info);
            request.getRequestDispatcher("user_profile.jsp").forward(request, response);
        } else {
            request.setAttribute("check", true);
            request.getRequestDispatcher("login.jsp").forward(request, response);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            int aid = Integer.parseInt(request.getParameter("aid"));
            String email = request.getParameter("email");
            String fullname = request.getParameter("fullname");
            int phone = Integer.parseInt(request.getParameter("phone"));
            
            Account ai = new Account();
            ai.setAid(aid);
            ai.setEmail(email);
            ai.setFullname(fullname);
            ai.setPhone(phone);
            
            AccountDAO accDb = new AccountDAO();
            accDb.updateAccountInfo(ai);
            response.sendRedirect("/WebApplication2/profile?success=1");
        } catch (Exception e) {
            response.sendRedirect("/WebApplication2/profile?success=0");
        }
    }

