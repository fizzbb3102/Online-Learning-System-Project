package entity;

/**
 *
 * @author ductd
 */
public class ClassList {
    private int id;
    private String name;
    private String teacher;
    private String status;
    private String note;
    
    public ClassList() {
    }

    public ClassList(int id, String name, String teacher, String status, String note) {
        this.id = id;
        this.name = name;
        this.teacher = teacher;
        this.status = status;
        this.note = note;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTeacher() {
        return teacher;
    }

    public void setTeacher(String teacher) {
        this.teacher = teacher;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
    
    
}
